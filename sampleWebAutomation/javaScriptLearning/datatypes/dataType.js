// data type number

let x = 10
console.log(x)
console.log(typeof x)

// data type string
let y = "Hello, World!"
console.log(y)
console.log(typeof y)

// data type boolean
let z = true
console.log(z)
console.log(typeof z) // console.log(typeof(z)) // boolean

// ----------------- data type is dynamic in nature -----------------
var a="prakash"
console.log(a)
a= 100
console.log(a)
console.log(typeof a) // console.log(typeof(a)) // number

